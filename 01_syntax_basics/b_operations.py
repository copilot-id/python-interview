# Operations on variables
# Variables can be used in mathematical operations.
# The result of the operation can be stored in a variable.

# Operations
# + : Addition
# - : Subtraction
# * : Multiplication
# / : Division
# // : Floor Division
# % : Modulus
# ** : Exponentiation

# Example
a = 10
b = 20
c = a + b
print(c)

# Output
# 30

# Example
a = 10
b = 20
c = a - b
print(c)

# Output
# -10

# Example
a = 10
b = 20
c = a * b
print(c)

# Output
# 200

# Example
