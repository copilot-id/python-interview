# Type conversion
# Variables can be converted from one data type to another data type.
# The data type of a variable can be converted using the following functions:
# int() : Converts a variable to an integer.
# float() : Converts a variable to a float.
# str() : Converts a variable to a string.
# bool() : Converts a variable to a boolean.

# Example
a = 10
b = float(a)
print(b)

# Output
# 10.0

# Example
a = 10.5
b = int(a)
print(b)

# Output
# 10

# Example
a = 10
b = str(a)
print(b)

# Output
# 10

# Example
a = "10"
b = int(a)
print(b)

# Output
# 10

# Example
a = "10.5"
b = float(a)
print(b)

# Output
# 10.5

# Example
a = "True"
b = bool(a)
print(b)

# Output
# True

# Example
a = "False"
b = bool(a)
print(b)

# Output
# False

# Example
a = 0
b = bool(a)
