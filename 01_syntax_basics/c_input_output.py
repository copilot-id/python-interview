# The input() function
# The input() function is used to take input from the user.
# The input() function always returns a string value.

# Syntax
# input(prompt)

# Parameters
# prompt : A string that is written to the console. It is optional.

# Example
name = input("Enter your name: ") # Enter your name: Ridwan Halim
print(name)
